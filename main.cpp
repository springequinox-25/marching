std::vector<float> marching_cubes(
    std::function<float(float, float, float)> f,
    float isovalue,
    float min,
    float max,
    float stepsize) {

    std::vector<float> vertices;

    // Iterate through each cube in the grid
    for (float x = min; x < max; x += stepsize) {
        for (float y = min; y < max; y += stepsize) {
            for (float z = min; z < max; z += stepsize) {
                // Calculate scalar values at cube corners
                float val[8];
                val[0] = f(x, y, z);
                val[1] = f(x + stepsize, y, z);
                val[2] = f(x + stepsize, y, z + stepsize);
                val[3] = f(x, y, z + stepsize);
                val[4] = f(x, y + stepsize, z);
                val[5] = f(x + stepsize, y + stepsize, z);
                val[6] = f(x + stepsize, y + stepsize, z + stepsize);
                val[7] = f(x, y + stepsize, z + stepsize);

                // Determine the index into the edge table
                int cubeindex = 0;
                if (val[0] < isovalue) cubeindex |= 1;
                if (val[1] < isovalue) cubeindex |= 2;
                if (val[2] < isovalue) cubeindex |= 4;
                if (val[3] < isovalue) cubeindex |= 8;
                if (val[4] < isovalue) cubeindex |= 16;
                if (val[5] < isovalue) cubeindex |= 32;
                if (val[6] < isovalue) cubeindex |= 64;
                if (val[7] < isovalue) cubeindex |= 128;

                // Use marching cubes lookup table to create triangles
                for (int i = 0; marching_cubes_lut[cubeindex][i] != -1; i += 3) {
                    for (int j = 0; j < 3; j++) {
                        int index = marching_cubes_lut[cubeindex][i + j];
                        float v1[3] = {x + vertTable[index][0] * stepsize,
                                       y + vertTable[index][1] * stepsize,
                                       z + vertTable[index][2] * stepsize};
                        vertices.insert(vertices.end(), std::begin(v1), std::end(v1));
                    }
                }
            }
        }
    }

    return vertices;
}

std::vector<float> compute_normals(const std::vector<float>& vertices) {
    std::vector<float> normals;

    // Iterate through each triangle (every 9 vertices)
    for (size_t i = 0; i < vertices.size(); i += 9) {
        // Calculate normal vector for the triangle
        float v1[3] = {vertices[i], vertices[i + 1], vertices[i + 2]};
        float v2[3] = {vertices[i + 3], vertices[i + 4], vertices[i + 5]};
        float v3[3] = {vertices[i + 6], vertices[i + 7], vertices[i + 8]};
        float edge1[3], edge2[3];
        for (int j = 0; j < 3; j++) {
            edge1[j] = v2[j] - v1[j];
            edge2[j] = v3[j] - v1[j];
        }
        float normal[3] = {
            edge1[1] * edge2[2] - edge1[2] * edge2[1],
            edge1[2] * edge2[0] - edge1[0] * edge2[2],
            edge1[0] * edge2[1] - edge1[1] * edge2[0]
        };
        // Normalize normal vector
        float length = sqrt(normal[0] * normal[0] + normal[1] * normal[1] + normal[2] * normal[2]);
        normal[0] /= length;
        normal[1] /= length;
        normal[2] /= length;
        // Add normal vector thrice for each vertex
        for (int j = 0; j < 3; j++) {
            normals.insert(normals.end(), std::begin(normal), std::end(normal));
        }
    }

    return normals;
}

void writePLY(const std::vector<float>& vertices, const std::vector<float>& normals, const std::string& fileName) {
    std::ofstream plyFile(fileName);
    if (!plyFile.is_open()) {
        std::cerr << "Error: Unable to open file for writing." << std::endl;
        return;
    }

    // Write PLY header
    plyFile << "ply\n";
    plyFile << "format ascii 1.0\n";
    plyFile << "element vertex " << vertices.size() / 3 << "\n";
    plyFile << "property float x\n";
    plyFile << "property float y\n";
    plyFile << "property float z\n";
    plyFile << "property float nx\n";
    plyFile << "property float ny\n";
    plyFile << "property float nz\n";
    plyFile << "element face " << vertices.size() / 9 << "\n";
    plyFile << "property list uchar int vertex_index\n";
    plyFile << "end_header\n";

    // Write vertices and normals
    for (size_t i = 0; i < vertices.size(); i += 3) {
        plyFile << vertices[i] << " " << vertices[i + 1] << " " << vertices[i + 2] << " "
                << normals[i] << " " << normals[i + 1] << " " << normals[i + 2] << "\n";
    }

    // Write faces
    for (size_t i = 0; i < vertices.size() / 9; i++) {
        plyFile << "3 " << 3 * i << " " << 3 * i + 1 << " " << 3 * i + 2 << "\n";
    }

    plyFile.close();

    std::cout << "PLY file written successfully: " << fileName << std::endl;
}

