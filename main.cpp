float cameraRadius = 5.0f;
float cameraTheta = glm::radians(45.0f);
float cameraPhi = glm::radians(45.0f);
float cameraSpeed = 0.01f;
bool dragging = false;
double lastX, lastY;

void mouse_callback(GLFWwindow* window, double xpos, double ypos) {
    if (dragging) {
        float sensitivity = 0.005f;
        float offsetX = xpos - lastX;
        float offsetY = lastY - ypos;
        offsetX *= sensitivity;
        offsetY *= sensitivity;

        cameraTheta += offsetX;
        cameraPhi += offsetY;

        cameraPhi = glm::clamp(cameraPhi, 0.1f, glm::pi<float>() - 0.1f);

        lastX = xpos;
        lastY = ypos;
    }
}

void mouse_button_callback(GLFWwindow* window, int button, int action, int mods) {
    if (button == GLFW_MOUSE_BUTTON_LEFT && action == GLFW_PRESS) {
        dragging = true;
        glfwGetCursorPos(window, &lastX, &lastY);
    } else if (button == GLFW_MOUSE_BUTTON_LEFT && action == GLFW_RELEASE) {
        dragging = false;
    }
}

void processInput(GLFWwindow* window) {
    if (glfwGetKey(window, GLFW_KEY_ESCAPE) == GLFW_PRESS)
        glfwSetWindowShouldClose(window, true);

    // Zoom in/out with arrow keys
    if (glfwGetKey(window, GLFW_KEY_UP) == GLFW_PRESS) {
        cameraRadius -= cameraSpeed;
        cameraRadius = glm::max(cameraRadius, 0.5f);
    }
    if (glfwGetKey(window, GLFW_KEY_DOWN) == GLFW_PRESS) {
        cameraRadius += cameraSpeed;
    }
}

const char* vertexShaderSource = R"(
    #version 400

    // Input vertex data, different for all executions of this shader.
    layout(location = 0) in vec3 vertexPosition_modelspace;
    layout(location = 1) in vec3 vertexNormal_modelspace;

    // Output data ; will be interpolated for each fragment.
    out vec3 Position_worldspace;
    out vec3 Normal_cameraspace;
    out vec3 EyeDirection_cameraspace;
    out vec3 LightDirection_cameraspace;

    // Values that stay constant for the whole mesh.
    uniform mat4 MVP;
    uniform mat4 V;
    uniform mat4 M;
    uniform vec3 LightDir; // Direction of the light source

    void main(){

        // Output position of the vertex, in clip space : MVP * position
        gl_Position =  MVP * vec4(vertexPosition_modelspace, 1.0);

        // Position of the vertex, in world space : M * position
        Position_worldspace = (M * vec4(vertexPosition_modelspace, 1.0)).xyz;

        // Vector that goes from the vertex to the camera, in camera space.
        // In camera space, the camera is at the origin (0,0,0).
        vec3 vertexPosition_cameraspace = ( V * M * vec4(vertexPosition_modelspace, 1.0)).xyz;
        EyeDirection_cameraspace = vec3(0, 0, 0) - vertexPosition_cameraspace;

        // Vector that goes from the vertex to the light, in camera space. M is omitted because it's identity.
        LightDirection_cameraspace = LightDir;

        // Normal of the vertex, in camera space
        Normal_cameraspace = mat3(transpose(inverse(V * M))) * vertexNormal_modelspace; // Corrected normal transformation
    }

)";

const char* fragmentShaderSource = R"(
    #version 400

    // Interpolated values from the vertex shaders
    in vec3 Position_worldspace;
    in vec3 Normal_cameraspace;
    in vec3 EyeDirection_cameraspace;
    in vec3 LightDirection_cameraspace;

    // Output data
    out vec4 color;

    uniform vec4 modelColor; // Base color for the diffuse component
    uniform float alpha;

    void main(){

        // Light emission properties
        vec4 LightColor = vec4(1.0, 1.0, 1.0, 1.0);

        // Material properties
        vec4 MaterialDiffuseColor = modelColor;
        vec4 MaterialAmbientColor = vec4(0.2, 0.2, 0.2, modelColor.a) * MaterialDiffuseColor;
        vec4 MaterialSpecularColor = vec4(1.0, 1.0, 1.0, modelColor.a); // Specular color

        // Normal of the computed fragment, in camera space
        vec3 n = normalize(Normal_cameraspace);

        // Direction of the light (from the fragment to the light)
        vec3 l = normalize(LightDirection_cameraspace);

        // Cosine of the angle between the normal and the light direction,
        // clamped above 0
        float cosTheta = clamp(dot(n, l), 0.0, 1.0);

        // Eye vector (towards the camera)
        vec3 E = normalize(EyeDirection_cameraspace);

        // Direction in which the triangle reflects the light
        vec3 R = reflect(-l, n);

        // Cosine of the angle between the Eye vector and the Reflect vector,
        // clamped to 0
        float cosAlpha = clamp(dot(E, R), 0.0, 1.0);

        // Phong shading model
        color =
            // Ambient : simulates indirect lighting
            MaterialAmbientColor +
            // Diffuse : "color" of the object
            MaterialDiffuseColor * LightColor * cosTheta +
            // Specular : reflective highlight, like a mirror
            MaterialSpecularColor * LightColor * pow(cosAlpha, alpha);
    }
)";

GLuint setupShader() {
    GLuint vertexShader = glCreateShader(GL_VERTEX_SHADER);
    glShaderSource(vertexShader, 1, &vertexShaderSource, NULL);
    glCompileShader(vertexShader);

    GLuint fragmentShader = glCreateShader(GL_FRAGMENT_SHADER);
    glShaderSource(fragmentShader, 1, &fragmentShaderSource, NULL);
    glCompileShader(fragmentShader);

    GLuint shaderProgram = glCreateProgram();
    glAttachShader(shaderProgram, vertexShader);
    glAttachShader(shaderProgram, fragmentShader);
    glLinkProgram(shaderProgram);

    glDeleteShader(vertexShader);
    glDeleteShader(fragmentShader);

    return shaderProgram;
}

void setupBuffer(GLuint& VAO, GLuint& VBO, GLuint& normalsVBO, const std::vector<float>& vertices, const std::vector<float>& normals) {
    glGenVertexArrays(1, &VAO);
    glGenBuffers(1, &VBO);
    glGenBuffers(1, &normalsVBO);

    glBindVertexArray(VAO);

    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, vertices.size() * sizeof(float), vertices.data(), GL_STATIC_DRAW);

    glBindBuffer(GL_ARRAY_BUFFER, normalsVBO);
    glBufferData(GL_ARRAY_BUFFER, normals.size() * sizeof(float), normals.data(), GL_STATIC_DRAW);

    glEnableVertexAttribArray(0);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);

    glEnableVertexAttribArray(1);
    glBindBuffer(GL_ARRAY_BUFFER, normalsVBO);
    glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);
}

